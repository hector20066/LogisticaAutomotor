    private void btn_ordenarListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ordenarListaActionPerformed
        try{
            int opcion = cbo_metodoOrdenamiento.getSelectedIndex();
            
            switch(opcion){
                case 0:
                    manejo.ordenamientoShell();
                    cargarAutos();
                    cargarCamiones();
                    cargarMotos();
                    cargarVehiculos();
                    break;
                case 1:
                    manejo.ordenamientoIntercambiado();
                    cargarAutos();
                    cargarCamiones();
                    cargarMotos();
                    cargarVehiculos();
                    break;
                case 2:
                    manejo.ordenamientoBurbuja();
                    cargarAutos();
                    cargarCamiones();
                    cargarMotos();
                    cargarVehiculos();
                    break;
                case 3:
                    manejo.ordenarSeleccion();
                    cargarAutos();
                    cargarCamiones();
                    cargarMotos();
                    cargarVehiculos();
                    break;
            }
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error al ordenar la lista.", null, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_ordenarListaActionPerformed

