    private void btn_buscarMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarMotoActionPerformed
        try{
            int posicion = manejo.busquedaBinaria(txt_buscarMoto.getText());
            Vehiculo vehiculo;
            
            if(manejo.getLista(posicion).getTipoVehiculo().equals("Motocicleta") && posicion != -1){
                vehiculo = manejo.getLista(posicion);

                modeloMotos.setRowCount(0);
                String placa = vehiculo.getPlaca();
                String marca = vehiculo.getMarca();
                int modelo = vehiculo.getModelo();
                float valor = vehiculo.getValorComercial();

                Object lista[] = new Object[]{placa, marca, modelo, valor};
                modeloMotos.addRow(lista);
            }else{
                JOptionPane.showMessageDialog(null, "La placa ingresada no pertenece a una moto.", null, JOptionPane.ERROR_MESSAGE);
                txt_buscarMoto.grabFocus();
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_buscarMotoActionPerformed

