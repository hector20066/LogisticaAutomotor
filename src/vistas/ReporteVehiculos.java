    private void cargarVehiculos(){
        listaGeneral.setRowCount(0);
        for(int i=0; i < manejo.getPosicion(); i++){
            Vehiculo vehiculos;
            
            vehiculos = manejo.getLista(i);
            String tipoVehiculo = vehiculos.getTipoVehiculo();
            String placa = vehiculos.getPlaca();
            String marca = vehiculos.getMarca();
            int modelo = vehiculos.getModelo();
            float valor = vehiculos.getValorComercial();

            Object lista[] = new Object[]{tipoVehiculo, placa, marca, modelo, valor};
            listaGeneral.addRow(lista);
        }
    }
    
